public class ByteShortExample {
    public static void main(String[] args) {
        // declare and initialize a byte variable
        byte myByte = 127;
        System.out.println("Byte value: " + myByte);

        // increment the byte variable
        myByte++;
        System.out.println("Incremented byte value: " + myByte);

        // declare and initialize a short variable
        short myShort = 32767;
        System.out.println("Short value: " + myShort);

        // increment the short variable
        myShort++;
        System.out.println("Incremented short value: " + myShort);
    }
}
